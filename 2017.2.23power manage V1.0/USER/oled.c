void LED_P6x8Char(byte x,byte y,byte ch)
{
	byte c=0,i=0;     
	c =ch-32;
	
	if(x>122)
	{
		x=0;
		y++;
	}			
	lcd_address(x,y);    
	for(i=0;i<6;i++)
	{     
		transfer_data_lcd(F6x8[c][i]);  
	}
}

//==============================================================
//函数名：LED_P6x8Str(byte x,byte y,byte *p)
//功能描述：写入一组6x8标准ASCII字符串
//参数：x为显示的横坐标0~122，y为页范围0～7，要显示的字符串
//返回：无
//==============================================================  

void LED_P6x8Str(byte x,byte y,byte ch[])
{
	byte c=0,i=0,j=0;     
	while (ch[j]!='\0')
	{    
		c =ch[j]-32;
		if(x>126)
		{
			x=0;
			y++;
		}
		lcd_address(x,y);    
		for(i=0;i<6;i++)
		{     
			transfer_data_lcd(F6x8[c][i]);  
		}
		x+=6;
		j++;
	}
}
